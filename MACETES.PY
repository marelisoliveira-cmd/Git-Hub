# Atalhos e Macetes de Produtividade no VS Code
#
# 1. Navegação
# - Abrir qualquer arquivo rápido: Ctrl + P / Cmd + P
# - Ir para função/classe dentro do arquivo: Ctrl + Shift + O / Cmd + Shift + O
# - Ir para uma linha específica: Ctrl + G / Cmd + G
# - Alternar arquivos abertos: Ctrl + Tab / Cmd + Tab
# - Dividir tela: Ctrl + \ / Cmd + \
#
# 2. Edição de linhas
# - Mover linha para cima/baixo: Alt + ↑/↓ / Option + ↑/↓
# - Deletar linha: Ctrl + Shift + K / Cmd + Shift + K
#
# 5. Busca e substituição
# - Buscar no arquivo: Ctrl + F / Cmd + F
# - Buscar e substituir: Ctrl + H / Cmd + H

